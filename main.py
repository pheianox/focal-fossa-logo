# Draw lines between given points
def draw_line(*points, stroke_color="black", fill_color="white"):

  # Set colors
  color(stroke_color, fill_color)

  # Start filling bg
  begin_fill()

  # Loop through points
  for i, point in  enumerate(points):

    # Draw first point
    if i == 0:
      penup()
      goto(point[0]- window_width/2, -1 * (point[1] - window_height/2))
      pendown()

    # Draw last point
    elif i == len(points)-1:
      goto(point[0]- window_width/2, -1 * (point[1] - window_height/2))
      goto(points[0][0]- window_width/2, -1 * (points[0][1] - window_height/2))

    # Draw points in between
    else:
      goto(point[0]- window_width/2, -1 * (point[1] - window_height/2))
  
  # Stop filling bg
  end_fill()
      
    
